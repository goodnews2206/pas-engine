"""
WebSocket Handler — real-time audio pipeline per call.
Twilio audio in → Deepgram STT → PAS Engine → ElevenLabs TTS → Twilio audio out

Post-call:
  - Lead memory upserted in Supabase
  - Call summary generated by Claude
  - Slack summary sent to brokerage channel
  - Agent appointment reminder sent via SMS + Slack
"""

